        Option2Click,
        /// <summary>
        /// The fourth right-click option.
        /// </summary>
        Option4Click,
        /// <summary>
        /// The third right-click option (e.g., "Trade").
        /// </summary>
        Option3Click,
        /// <summary>
        /// The first right-click option (e.g., "Talk-to").
        /// </summary>
        Option1Click,
        /// <summary>
        /// The fifth right-click option.
        /// </summary>
        Option5Click,
        /// <summary>
        /// The sixth right-click option.
        /// </summary>
        Option6Click,
    }
}