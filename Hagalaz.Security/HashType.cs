namespace Hagalaz.Security
{
    /// <summary>
    /// Defines the supported hash algorithms.
    /// </summary>
    public enum HashType
    {
        /// <summary>
        /// The MD5 hash algorithm.
        /// </summary>
        MD5,
        /// <summary>
        /// The SHA1 hash algorithm.
        /// </summary>
        SHA1,
        /// <summary>
        /// The SHA256 hash algorithm.
        /// </summary>
        SHA256,
        /// <summary>
        /// The SHA384 hash algorithm.
        /// </summary>
        SHA384,
        /// <summary>
        /// The SHA512 hash algorithm.
        /// </summary>
        SHA512
    }
}
